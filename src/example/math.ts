const sum = (a: number, b: number): number => a + b;
const mul = (a: number, b: number): number => a * b;
const sub = (a: number, b: number): number => a - b;
const div = (a: number, b: number): number => a / b;
const sqr = (a: number):number => a * a;

export default {
  sum, mul, sub, div, sqr,
};
